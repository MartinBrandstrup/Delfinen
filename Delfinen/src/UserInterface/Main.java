        try
        {
            c.saveTeamList();
        }
        catch (Exception ex)
        {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

        lastTeamAddedTable(); //is choosing which table the field model appear as

        Object rowData[] = new Object[1];

        rowData[0] = c.getLastAddedTeam().getTeamName();
